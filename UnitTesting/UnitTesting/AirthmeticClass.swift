import Foundation

class AirthmeticClass {
    
    func add(n1: Int, n2:Int) -> Int {
        return n1 + n2
    }
    
    func sub(n1: Int, n2:Int) -> Int {
        return n1 - n2
    }
    
    func dev(n1: Int, n2:Int) -> Int {
        return n1 / n2
    }
    func mul(n1: Int, n2:Int) -> Int {
        return n1 * n2
    }
    
}
